//Obtener la sumatoria, resta, multiplicación de un array de números.

//Definir array

const numeros = [1, 5, 6, 4, 9]

//Suma

let sum = 0;

for (const number of numeros) {

    sum += number;
}

console.log(`La sumatoria es ${sum}`);

//Resta

let resta = 0;

for (number of numeros)
{

    resta -= number
}

console.log(`La resta es ${resta}`);

//Multiplicacion

let mul = 1;

for(const numberos of numeros){
    mul *= numberos;
}

console.log(`La multiplicacion es ${mul}`);